class Car {
  final String name;
  final String image;
  final String? description;
  final String category;
  final String price;
  final String address;
  final List<String> features;

  Car({
    required this.name,
    required this.image,
    required this.description,
    required this.category,
    required this.price,
    required this.address,
    required this.features,
  });
}
